package clueGame;

import java.awt.Color;

public class Player {

	private String playerName;
	private Color color;
	private int row;
	private int col;

	// getter functions
	public String getplayerName() {
		return playerName;
	}
	public Color getColor() {
		return color;
	}
	public int getRow() {
		return row;
	}
	public int getCol() {
		return col;
	}
	
}
